async function getMangaDetail(slug) {
  try {
    const res = await fetch(
      "https://kurokami.vercel.app/api/manhwa-detail/" + slug,
      { method: "get", cache: "force-cache", next: { revalidate: 60 * 60 } }
    );
    return res.json();
  } catch (error) {
    throw new Error(error);
  }
}

